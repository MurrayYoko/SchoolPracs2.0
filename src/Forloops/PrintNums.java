package Forloops;

import javax.swing.*;
import java.awt.*;

public class PrintNums {

  public static void main (String[] args) {
  
  int num = 0 ;
  for (int loop = 1; loop <= 5; loop++)
  {
      num++;
      System.out.println ("The loop has been carried out" + num +"time(s).");
      System.out.println ();
      
   }
   System.out.println("The loop has ended now.");
  
  
  
  
  
  }
  
}
